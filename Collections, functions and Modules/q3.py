# Differentiate between append () and extend () methods? 

# append(): This method is used to add a single element to the end of a list. 
# It takes one argument, which is the element to be added. If the argument is another list, 
# it will be added as a single element at the end of the list.

# list1=[1,2,3,4]
# # list1.append(5)
# list1.append([5,6,7,8])
# print(list1)


# extend(): This method is used to add elements from an iterable (such as another list, 
# tuple, or string) to the end of the list. It takes one argument, 
# which is the iterable whose elements are to be added to the list

# list1=[1,2,3,4]
# list1.extend([5,6,7,8])
# print(list1)
